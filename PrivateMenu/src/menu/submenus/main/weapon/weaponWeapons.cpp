	void giveWeapon(char* hash) {
		if (!WEAPON::HasPedGotWeapon(GetLocalPlayer().m_ped, Utils::GetHashKey(hash), 0))
		{
			WEAPON::GiveDelayedWeaponToPed(GetLocalPlayer().m_ped, Utils::GetHashKey(hash), 9999, 1);
		}
		else {
			WEAPON::SetCurrentPedWeapon(GetLocalPlayer().m_ped, Utils::GetHashKey(hash), 1);
		}
	}

	void RainbowCamo() {
		if (GetLocalPlayer().m_hasWeaponInHand) {
			static int TIMER;
			Utils::DoTimedFunction(&TIMER, 1000, [] {
				WEAPON::SetPedWeaponTintIndex(GetLocalPlayer().m_ped, GetLocalPlayer().m_weaponHash, MISC::GetRandomIntInRange(0, 7));
			});
		}
	}

