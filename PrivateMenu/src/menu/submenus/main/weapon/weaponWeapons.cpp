void WeaponWeaponsMenu::Init() {
	setParentSubmenu<WeaponMenu>();
	setName("Weapons", true, true);

	addOption(ButtonOption("Get all weapons")
		.addFunction([] {
		int Ammo = -1;
		for (int i = 0; i < 87; i++) {
			WEAPON::GetMaxAmmo(GetLocalPlayer().m_ped, usableWeaponHashes[i], &Ammo);
			WEAPON::GiveWeaponToPed(GetLocalPlayer().m_ped, usableWeaponHashes[i], Ammo, false, true);
		}
	}).addTranslation().addHotkey()
		.addTooltip("Get all weapons"));

	addOption(ToggleOption("Get all weapons every session")
		.addToggle(vars.giveallweaponsloop)
		.addTranslation().addHotkey().canBeSaved()
		.addTooltip("This will give you all weapons on session load"));

	addOption(ButtonOption("Clear all weapons")
		.addFunction([] { WEAPON::RemoveAllPedWeapons(GetLocalPlayer().m_ped, true); })
		.addTooltip("Clear your inventory").addTranslation().addHotkey());

	addOption(BreakOption("Upgrades").addTranslation());

	addOption(ButtonOption("Upgrades (current weapon)")
		.addFunction([] {
		if (GetLocalPlayer().m_hasWeaponInHand) {
			for (int Component = 0; Component < 210; Component++) {
				if (WEAPON::DoesWeaponTakeWeaponComponent(GetLocalPlayer().m_weaponHash, weaponUpgrades[Component])) {
					WEAPON::GiveWeaponComponentToPed(GetLocalPlayer().m_ped, GetLocalPlayer().m_weaponHash, weaponUpgrades[Component]);
				}
			}
		}
	}).addTranslation().addHotkey()
		.addTooltip("Upgrade your current weapon"));

	addOption(ButtonOption("Upgrades (all weapons)")
		.addFunction([] {
		for (int i = 0; i < NUMOF(usableWeaponHashes) - 1; i++) {
			if (WEAPON::HasPedGotWeapon(GetLocalPlayer().m_ped, usableWeaponHashes[i], false)) {
				for (int Component = 0; Component < 210; Component++) {
					if (WEAPON::DoesWeaponTakeWeaponComponent(usableWeaponHashes[i], weaponUpgrades[Component])) {
						WEAPON::GiveWeaponComponentToPed(GetLocalPlayer().m_ped, usableWeaponHashes[i], weaponUpgrades[Component]);
					}
				}
			}
		}
	}).addTranslation().addHotkey()
		.addTooltip("Upgrade your current weapon"));

	addOption(BreakOption("Color Edits").addTranslation());

	addOption(ScrollOption<int>(SCROLL, "Camo (current weapon)")
		.addScroll(vars.camo, 0, NUMOF(camoScrollStruct) - 1, camoScrollStruct)
		.addFunction([] { WEAPON::SetPedWeaponTintIndex(GetLocalPlayer().m_ped, GetLocalPlayer().m_weaponHash, vars.camo); })
		.addTooltip("Set the camo of your current weapon").addTranslation().addHotkey());

	addOption(ScrollOption<int>(SCROLL, "Camo (all weapons)")
		.addScroll(vars.camoAll, 0, NUMOF(camoScrollStruct) - 1, camoScrollStruct)
		.addFunction([] { 
			for (int i = 0; i < NUMOF(usableWeaponHashes) - 1; i++) {
				WEAPON::SetPedWeaponTintIndex(GetLocalPlayer().m_ped, usableWeaponHashes[i], vars.camoAll);
			}
		}).addTranslation().addHotkey()
		.addTooltip("Set all weapons camo"));

	addOption(ToggleOption("Rainbow Camo (current weapon)")
		.addToggle(vars.rainbowCamo)
		.addTooltip("Spam rainbow camo").addTranslation().addHotkey());
}

